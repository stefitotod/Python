# Напишете програма, в която се създава числов списък. Той се запълва със случайни числа. 
# Между всяка двойка елементи се вмъква нов елемент, който е равен на съсдените му елементи.

import random
n = int(input("n = "))
l = [random.randint(1,100) for i in range(n)]
n -= 1
while n > 0:
    l.insert(n, l[n] + l[n-1])
    n -=1
print(l)
