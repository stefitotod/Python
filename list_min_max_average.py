# Напишете програма с функция с произволен брой числови аргументи. 
# Като резултат връща списък от 3 елемента- средната, максималната, минималната стойност на 3-те елемента.

def my_function(*n): 
    result_elements = [max(n), min(n), sum(n)/len(n)]
    return result_elements

print(my_function(1,2,3,4,5,6))
