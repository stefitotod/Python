# Напишете програма, в която е описана функция дали цяло число е просто.
# Числото се предава като аргумент на функцията.
# Функцията връща резултат:
# 1- ако е просто.
# 0- ако не е.
# Да се принтират всички прости числа от 2 до това число.

def is_prime(num):
    for x in range(2, num):
        if (num % x) == 0:
            return 0
    return 1

num = 13

for x in range(2, num + 1):
    if is_prime(x):
        print(x)
